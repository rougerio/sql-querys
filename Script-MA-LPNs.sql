AND LPN.inbound_outbound_indicator = 'I'
AND FAC.inbound_outbound_indicator = 'I'
AND LPN.LPN_STATUS = 45
AND DET.ITEM_NAME IN ('BUR22Z24PZALHCT',
'BUR22Z24PZBBHCT',
'BUR22Z24PZGRHCT',
'BUR22Z24PZSKHCT')
GROUP BY LPN.TC_LPN_ID, LPN.LPN_STATUS, STAT.DESCRIPTION, FAC.DESCRIPTION, DET.ITEM_NAME
UNION ALL 
