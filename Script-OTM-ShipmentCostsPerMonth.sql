-------------------------------Invoices-------------------------------

SELECT V.*--IV.INVOICE_GID, IVS.SHIPMENT_GID, V. 
FROM INVOICE IV INNER JOIN
INVOICE_LINEITEM IVL ON IV.INVOICE_GID = IVL.INVOICE_GID INNER JOIN
INVOICE_SHIPMENT IVS ON IV.INVOICE_GID = IVS.INVOICE_GID INNER JOIN
INVOICE_LINEITEM_BOV V ON IV.INVOICE_GID = V.INVOICE_GID
WHERE IV.DOMAIN_NAME = 'NBL/MX'
AND IV.INSERT_DATE >= TO_DATE('01/02/2026', 'DD/MM/YYYY')
AND IVL.DESCRIPTION = 'A'


SELECT * 
FROM INVOICE_LINEITEM_BOV  
WHERE INSERT_DATE >= TO_DATE('01/02/2026', 'DD/MM/YYYY')