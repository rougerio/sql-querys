
--------------------------------Shipment Costs-------------------------------

SELECT SC.SHIPMENT_GID, OM.ORDER_RELEASE_GID, SC.COST_TYPE, SC.ACCESSORIAL_CODE_GID, SC.ADJUSTMENT_REASON_GID, SC.COST,
SC.INSERT_DATE, SC.INSERT_USER
FROM SHIPMENT_COST SC INNER JOIN
ORDER_MOVEMENT OM ON SC.SHIPMENT_GID = OM.SHIPMENT_GID
WHERE 1 = 1
AND SC.DOMAIN_NAME = 'NBL/MX'
AND SC.COST_TYPE = 'A'
AND SC.INSERT_DATE >= TO_DATE('01/02/2026', 'DD/MM/YYYY')
ORDER BY SC.INSERT_DATE ASC


