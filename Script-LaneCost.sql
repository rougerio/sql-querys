--Query para conocer las asignaciones


SELECT SHIPMENT_XID, SERVPROV_GID, RATE_OFFERING_GID, RATE_GEO_GID
FROM SHIPMENT S INNER JOIN
SHIPMENT_STOP SS ON S.SHIPMENT.GID = SS.SHIPMENT_GID
WHERE SHIPMENT_XID = 'MX51079539';

