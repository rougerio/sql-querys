SELECT SH.SHIPMENT_GID
FROM SHIPMENT SH INNER JOIN 
SHIPMENT_STATUS SSH ON SH.SHIPMENT_GID = SSH.SHIPMENT_GID
WHERE SH.BULK_PLAN_GID LIKE '%OR_BULKPLAN_EDM%' 
AND TO_CHAR(SH.INSERT_DATE, 'YYYY-MM-DD') = TO_CHAR(SYSDATE, 'YYYY-MM-DD')
AND SSH.STATUS_TYPE_GID = 'NBL/MX.SECURE RESOURCES'
AND SSH.STATUS_VALUE_GID = 'NBL/MX.SECURE RESOURCES_NOT STARTED'



