SELECT ORS.ORDER_RELEASE_GID 
FROM ORDER_RELEASE ORS INNER JOIN 
BULK_PLAN BP ON ORS.BULK_PLAN_GID = BP.BULK_PLAN_GID INNER JOIN 
ORDER_RELEASE_STATUS ORSS ON ORS.ORDER_RELEASE_GID = ORSS.ORDER_RELEASE_GID AND ORSS.STATUS_TYPE_GID = 'NBL/MX.PLANNING' AND ORSS.STATUS_VALUE_GID = 'NBL/MX.PLANNING_PLANNED - FAILED'
WHERE ORS.BULK_PLAN_GID = 'NBL/MX.20250618-0001'

