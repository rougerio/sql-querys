SELECT /*C.CARRIER_ID, A.VENDOR_ID, */ A.VENDOR_NAME, C.FREIGHT_CODE, C.SCAC_CODE /*, C.CURRENCY_CODE, S.VENDOR_SITE_CODE, S.VENDOR_SITE_ID*/
