SELECT  RG.X_LANE_GID
        ,TO_CHAR(ROUND(MIN(RGC.CHARGE_AMOUNT)), 'fm9999G999') MIN
        ,TO_CHAR(ROUND(MAX(RGC.CHARGE_AMOUNT)), 'fm9999G999') MAX
        ,TO_CHAR(ROUND(AVG(RGC.CHARGE_AMOUNT)), 'fm9999G999') AVG
        --,RGC.CHARGE_AMOUNT
FROM RATE_GEO_COST RGC INNER JOIN
    RATE_GEO_COST_GROUP RGCG ON RGC.RATE_GEO_COST_GROUP_GID = RGCG.RATE_GEO_COST_GROUP_GID INNER JOIN
    RATE_GEO RG ON RG.RATE_GEO_GID = RGCG.RATE_GEO_GID
WHERE RGC.CHARGE_AMOUNT > 73100
AND RG.DOMAIN_NAME = 'NBL/MX' 
AND RG.IS_ACTIVE = 'Y'
AND RG.EXPIRATION_DATE IS NULL 
GROUP BY RG.X_LANE_GID
ORDER BY RG.X_LANE_GID


X_LANE_GID IN ('NBL/MX.1191329','NBL/MX.1206335','NBL/MX.1213334','NBL/MX.1213335','NBL/MX.1213336','NBL/MX.1284367','NBL/MX.1284368')